                                            <p><img alt="" src="{{ asset('web/uploads/abount.png')}}"
                                                    style="height: 100%; width: 100%;" /></p>

                                            <p><em>Welcome to Hanoi University of Science and Technology (HUST) – the
                                                    first prestigious and influential multidisciplinary technical
                                                    university in Vietnam!</em></p>

                                            <p>Since&nbsp;establishment of university, HUST has been committed to
                                                research, knowledge development and intellectual training for the
                                                society. The university&nbsp;gains the reputation of being the first and
                                                the leading technical university in Vietnam thanks to generations of
                                                influential alumni.</p>

                                            <p>With our great traditions and values, HUST maintains the teaching and
                                                learning quality while embracing innovative changes to meet the needs of
                                                students and society, especially in the era of digital economy and
                                                globalization.</p>

                                            <p>The development strategies&nbsp;for the period of 2017 – 2025
                                                have&nbsp;been implemented effectively for 3 years and strategic plans
                                                have been recognized for the period of 2020 – 2025.</p>

                                            <p>HUST has earned a reputation for an influential&nbsp;university in the
                                                region and the world thanks to high-quality and well-trained lecturers,
                                                modern facilities, and research achievements.</p>

                                            <p>HUST is also known as the most attractive destination for talented
                                                students in science, engineering, and technology. As a higher education
                                                institution, HUST was accredited in 2017 by HCERES. In 2020, HUST was in
                                                the top of best universities&nbsp;in Asia&nbsp;according to Times Higher
                                                Education.</p>

                                            <p>For QS WUR by Subject 2020, Electrical and Electronics Engineering and
                                                Mechanical, Aeronautical Engineering &amp; Manufacturing are in the
                                                ranks of 351-400; Computer Science &amp; Information System, and
                                                Mathematics are from 451 to 500. HUST continues to affirm its pioneering
                                                position in Vietnam.</p>

                                            <p>While the Fourth Industrial Revolution opens up great opportunities,
                                                it&nbsp;also brings about unpredictable challenges.&nbsp;HUST continues
                                                its noble mission to research, knowledge development and intellectual
                                                training for the society.</p>

                                            <p>With a strong commitment, HUST maintains innovations for becoming the
                                                leading regional research university in engineering, science&nbsp;and
                                                technology and a model of comprehensive autonomous university with
                                                sustainable development, making significant contribution to the society
                                                and Vietnam higher education.</p>

                                            <p>An attractive campus and a professional working environment make HUST a
                                                trustworthy home with respect, solidarity, dedication for students,
                                                staff, and lecturers to together bring about innovation, breakthroughs,
                                                and success.</p>

                                            <p><em><strong><span style="font-family:calibri,sans-serif;"><span
