            element.classList.add("animate__slow");
            element.classList.add("animate__fadeOutRight");
            element.classList.add("js-animate-bg");
        };

        const displayScrollElement_Right = (element) => {
            element.classList.add("animate__animated");
            element.classList.add("animate__slow");
            element.classList.add("animate__fadeOutLeft");
            element.classList.add("js-animate-bg");
        };

        const hideScrollElement = (element) => {
            element.classList.remove("animate__animated");
            element.classList.remove("animate__fadeOutRight");
            element.classList.remove("animate__slow");
            element.classList.remove("js-animate-bg");
        };

        const hideScrollElement_Right = (element) => {
            element.classList.remove("animate__animated");
            element.classList.remove("animate__fadeOutLeft");
            element.classList.remove("animate__slow");
            element.classList.remove("js-animate-bg");
        };

        const handleScrollAnimation = () => {
            scrollElements_Left.forEach((el) => {
                if (elementInView(el, 1)) {
                    displayScrollElement(el);
                } else if (elementOutofView(el)) {
                    hideScrollElement(el)
                }
            })
        }

        const handleScrollAnimation_Right = () => {
            scrollElements_Right.forEach((el) => {
                if (elementInView(el, 1)) {
                    displayScrollElement_Right(el);
                } else if (elementOutofView(el)) {
                    hideScrollElement_Right(el)
                }
            })
        }

        window.addEventListener("scroll", () => {
            handleScrollAnimation();
            handleScrollAnimation_Right();
        });
